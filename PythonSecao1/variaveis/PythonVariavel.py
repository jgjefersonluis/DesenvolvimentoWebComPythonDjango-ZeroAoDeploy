# Uma variável é um nome que se refere a um valor.
# Um comando de atribuição cria uma nova variável e lhe dá um valor.
# Variáveis são usadas para guardarmos valores que serão usados mais tarde no programa.

var1 = 10
var2 = 5

print(var1)
print(var2)
idade_crianca = 5
